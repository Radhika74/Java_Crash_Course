public class loops {
    public static void main(String[] args) {
        for (int i=0; i<4; i++){
            System.out.print("Hello\n");
        }
        int i=0;
        while(i<11){
            System.out.println(i);
            i++;
        }
        do{
            System.out.println(i);
            i++;
        }
        while(i<11);{
            System.out.println("Hello");
        }
        
    }
}
